unsigned char Find_font16(unsigned char *str)
{
	unsigned short idenx;
	idenx = (str[0] << 8) | str[1];
	switch (idenx) {
        case 0xCAB1: return 0;
        case 0xBCE4: return 1;
        case 0xBEAD: return 2;
        case 0xB6C8: return 3;
        case 0xCEB3: return 4;
        case 0xCEC0: return 5;
        case 0xD0C7: return 6;
        case 0xBEAB: return 7;
        case 0xB1B1: return 8;
        case 0xB6B7: return 9;
        case 0xB5BC: return 10;
        case 0xBABD: return 11;
        case 0xCFB5: return 12;
        case 0xCDB3: return 13;
        case 0xCFF2: return 14;
        case 0xA1E3: return 15;
        default: return 0;
	}
}

